public class StringExercise {
    public static void main(String[] args){

        String education = "We don't need no education";
        System.out.println(education.replace("education", "thought control"));


        String checkThisOut = "Check \"this\" out!, \"s inside of \"s!\n ";
        System.out.println(checkThisOut);

        String windows = "In windows, the main drive is usually C:\\\n";
        System.out.println(windows);

        String backSlash = "I can do backslashes \\, double backslashes \\\\, \n and the amazing triple backslash \\\\\\!\n";
        System.out.println(backSlash);
    }

}



